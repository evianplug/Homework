# First Line
file = open('text.txt')
print("First line:")
print(file.readline())
FB = []

first = int(input("First number:"))
second = int(input("Second number:"))
third = int(input("Third number:"))
for i in range(first, third + 1):
    entry = ''
    if i % 2 == 0:
        entry += "F"
    if i % 5 == 0:
        entry += "B"
    if i % 2 != 0 and i % 5 != 0:
        entry = i

    FB.append(entry)

for i in FB:
    print(i)

